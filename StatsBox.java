
public class StatsBox {
	
	private String rank = "";
	private String rating = "";
	
	public StatsBox(){}
	
	public void setRank(String Rank) {
		this.rank = Rank;
	}
	
	public String getRank() {
		return rank;
	}
	
	public String getRating() {
		return rating;
	}
	
	public void setRating(String rating) {
		this.rating = rating;
	}
	

}
